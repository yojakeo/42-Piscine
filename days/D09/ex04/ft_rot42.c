char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	if (!str[i])
		return (0);
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && 'J' >= str[i])
			str[i++] += 16;
		if (str[i] >= 'K' && 'Z' >= str[i])
			str[i++] -= 10;
		if (str[i] >= 'a' && 'j' >= str[i])
			str[i++] += 16;
		if (str[i] >= 'k' && 'z' >= str[i])
			str[i++] -= 10;
	}
	return (str);
}
