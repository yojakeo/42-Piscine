int		ft_is_prime(int nb)
{
	int i;
	int j;
	int op;

	i = 0;
	j = 1;
	while (nb)
	{
		op = i * j;
		if (nb == op)
			return (0);
		else if (op > nb)
		{
			j++;
			i = 0;
		}
		if (j > nb & i > nb)
			return (1);
		i++;
	}
	return (0);
}
