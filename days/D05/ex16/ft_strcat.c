char	*ft_strcat(char *dest, char *src)
{
	int i;
	int j;

	i = 0;
	while (dest[i] != '\0')
		i++;
	j = -1;
	while (src[++j] != '\0')
		dest[i++] = src[j];
	if (src[j] == '\0')
		dest[i] = '\0';
	return (dest);
}
