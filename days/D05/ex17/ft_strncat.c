char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int j;

	i = 0;
	while (dest[i] != '\0' && i <= nb)
		i++;
	j = -1;
	while (src[++j] != '\0' && j <= nb)
		dest[i++] = src[j];
	if (src[j] == '\0')
		dest[i] = '\0';
	return (dest);
}
